package j0509;

public class Second {
	public static void main(String[] args) {
		 
		// 세미콜론 ; => 하나의 명령문의 끝을 알린다.
		
		System.out.println("안녕!!");  // 출력 후 줄바꿈 발생
		System.out.println("잘 가!!");
		
		System.out.println("============");
		
		System.out.print("안녕!!");    // 줄바꿈이 없다
		System.out.print("잘 가!!"); 
		System.out.println("하하하");
	}

	// 보라색은 예약어 => 자바에서 사용되는 단어
	// 파란색의 이탤릭체 => static method
}
